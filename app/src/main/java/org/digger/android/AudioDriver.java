package org.digger.android;

/**
 * Звуковые эффекты игры — перенос набора {@code soundXxx()} из оригинального
 * {@code Sound.java}, но не архитектуры (там — программная эмуляция PC Speaker
 * с тиковым таймером; здесь — заранее сгенерированные короткие сэмплы).
 *
 * <p>Каждый {@code playXxx()} — одноразовый "выстрелил и забыл": в отличие от
 * оригинала, где, например, звук выстрела тянется, пока летит снаряд, здесь
 * это сэмпл фиксированной длины, который просто доигрывает сам. Отдельного
 * метода остановки у конкретных эффектов поэтому нет — только {@link #stopAll()}
 * для паузы/сворачивания приложения.
 */
interface AudioDriver {

    void playFire();

    void playExplode();

    /** Короткий "тук" в момент касания изумруда. */
    void playEmeraldPickup();

    /**
     * Ступень восходящей гаммы при сборе изумруда — перенос {@code soundEmerald()}.
     *
     * @param step 0..6, номер ступени (см. {@link SquareWaveSynth} и вызывающую сторону
     *             про то, как считается номер ступени).
     */
    void playEmeraldScale(int step);

    void playGoldCollect();

    void playBagWobble();

    void playBagFall();

    void playBagBreak();

    void playDiggerDeath();

    void playExtraLife();

    void playLevelComplete();

    /**
     * Зацикленная фоновая мелодия уровня — перенос {@code music(1)}/{@code backgjingle}.
     * В отличие от остальных эффектов, это единственный "тянущийся" звук:
     * играет, пока не будет явно остановлен {@link #stopBackgroundMusic()}
     * (или {@link #stopAll()}) — оригинал держит её на отдельном от эффектов
     * канале (Timer0 vs Timer2), поэтому она не мешает остальным {@code playXxx()}.
     * Безопасно вызывать повторно, пока звучит — просто перезапускает петлю.
     */
    void playBackgroundMusic();

    /**
     * Останавливает фоновую мелодию — перенос {@code musicOff()}. Оригинал
     * зовёт её сразу по окончании партии (смерть/прохождение уровня), ДО
     * того, как заиграет похоронный марш или джингл уровня — они с фоновой
     * музыкой никогда не звучат одновременно.
     */
    void stopBackgroundMusic();

    void setMuted(boolean muted);

    boolean isMuted();

    /** Останавливает всё, что сейчас звучит — вызывается на паузе игры/приложения. */
    void stopAll();

    /** Освобождает нативные аудио-ресурсы — вызывается при уничтожении View/Activity. */
    void release();
}
